package GUI;
public class K {
    int freq;
    String label;

    public K() {
    }

    public K(int freq, String label) {
        this.freq = freq;
        this.label = label;
    }

    public int getFreq() {
        return freq;
    }

    public void setFreq(int freq) {
        this.freq = freq;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
