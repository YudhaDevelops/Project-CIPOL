    public void tampilData(){
        String[] header = {"Label Data Uji", "Label Tertebak"};
        Object[][] objDataTebak = new Object[listTebak.size()][2];
        int i = 0;
        for (Tebak n : listTebak) {
            if (n instanceof Tebak) {
                String[] arrData = {
                    n.labelDataUji,n.getLabelTebak()
                };
                objDataTebak[i] = arrData;
            }
            i++;
        }
        tabelTebak = new DefaultTableModel(objDataTebak, header) {
            //membuat tabel tidak bisa diedit saat diklik
            @Override
            public boolean isCellEditable(int rowIndex, int colIndex) {
                return false;
            }
        };
        jTable1.setModel(tabelTebak);
        columnModelTebak = jTable1.getColumnModel();
    }
    
